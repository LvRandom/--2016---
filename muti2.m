function y = muti2(x,n)
y(1,1) = 3*x(1)+(-1)*x(2)+(1/2)*x(n);
for i = 2:(n/2)-1
    y(i) = (-1)*x(i-1)+3*x(i)+(-1)*x(i+1)+0.5*x(n-i+1);
end
for i = (n/2):(n/2)+1
    y(i) = (-1)*x(i-1)+3*x(i)+(-1)*x(i+1);
end
for i = (n/2)+2:n-1
    y(i) = (-1)*x(i-1)+3*x(i)+(-1)*x(i+1)+0.5*x(n-i+1);
end
y(n) = 3*x(n)+(-1)*x(n-1)+0.5*x(1);
y = y';